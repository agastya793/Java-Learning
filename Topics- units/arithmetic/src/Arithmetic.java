public class Arithmetic {
    public static void main(String[] args) {
        int a = 8;
        int b = 7;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        double x = 8;
        double o = 9;
        System.out.println(x+o);
        System.out.println(x-o);
        System.out.println(x/o);
        System.out.println(x*o);

    }
}
